package net.arcor.bks.common;

public class BksFunctionalException extends Exception {

	public BksFunctionalException() {
		super();
	}

	public BksFunctionalException(String message, Throwable cause) {
		super(message, cause);
	}

	public BksFunctionalException(String message) {
		super(message);
	}

	public BksFunctionalException(Throwable cause) {
		super(cause);
	}

}
